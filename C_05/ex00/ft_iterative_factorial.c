int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (i == 0 || i == 1)
		return (1);
	else if (i < 0)
		return (0);
	while (i > 1)
	{
		nb *= (i - 1);
		i--;
	}
	return (nb);
}
