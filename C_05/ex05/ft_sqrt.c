int	ft_sqrt(int nb)
{
	float	n;
	float	m;

	n = 1;
	if (nb < 0)
		nb *= -1;
	m = nb;
	if (nb == 1)
		return (1);
	while (n < nb)
	{
		if ((m / n) == n)
			return (n);
		else if ((m / n) < n)
			return (0);
		n += 1;
	}
	return (0);
}
