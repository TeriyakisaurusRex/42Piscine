int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 1;
	if (nb <= 1)
		return (0);
	while (i < (nb / 2))
	{
		i++;
		j = nb % i;
		if (!j)
			return (0);
	}
	return (1);
}
